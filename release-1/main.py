def hiFunction(name):
    return 'Hi, ' + name

def sum(num_1, num_2):
    return num_1 + num_2

def isEven(num):
    return num % 2 == 0

def apples(amount):
    return f'i have {amount} apples'

def getPower(num):
    return num**2

print(hiFunction('Denis'))
print(sum(1, 2))
print(isEven(0))
print(apples(2))
print(getPower(4))